def maior(n1: int, n2: int):
    """
    1- Faça um Programa que peça dois números e imprima o maior deles.

    >>> maior (1,2)
    2

    >>> maior (20, 13)
    20
    
    :param n1: Prmeiro valor : inteiro
    :param n2: Segundo valor : inteiro
    :return:
    """
    return (n1 if n1 > n2 else n2)
