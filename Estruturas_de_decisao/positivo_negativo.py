def positivo_negativo(n: int):
    """
    2- Faça um Programa que peça um valor e mostre na tela se o valor é positivo ou negativo.
    >>> positivo_negativo(10)
    'Positivo'
    >>> positivo_negativo(-20)
    'Negativo'

    :param n: Numero a ser avaliado : inteiro
    :return:
    """

    return ('Positivo' if n > 0 else 'Negativo')
