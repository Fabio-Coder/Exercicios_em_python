def area_circulo(r: float):
    """
    6-    Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.

    >>> area_circulo(10)
    314.0

    >>> area_circulo(253.2)
    201306.1536

    :param n: Raio do circulo - float
    :return:  Area do circulo - float
    """

    return (3.14 * (r * r))
